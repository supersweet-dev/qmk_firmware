static void render_message(void) {
    switch (get_highest_layer(layer_state)) {
        case _CDH:
            oled_write_P(PSTR("supersweet\n"), false);
            oled_write_P(PSTR("owo\n"), false);
            oled_write_P(PSTR("c12-h22-o11\n"), false);
            break;
        case _LOWER:
            oled_write_P(PSTR("navigation\n"), false);
            oled_write_P(PSTR("nwn\n"), false);
            oled_write_P(PSTR("find your way!\n"), false);
            break;
        case _RAISE:
            oled_write_P(PSTR("^ & * ( )\n"), false);
            oled_write_P(PSTR("uwu\n"), false);
            oled_write_P(PSTR("special    "), false);
            break;
        case _ADJUST:
            oled_write_P(PSTR("*-*-*\n"), false);
            oled_write_P(PSTR("*-*-*\n"), false);
            oled_write_P(PSTR("*-*-*\n"), false);
            break;
        default:
            oled_write_P(PSTR("omg"), false);
            oled_write_P(PSTR("):\n"), false);
            oled_write_P(PSTR("get me outta here.\n"), false);
    }
}
static void render_status(void) {
    switch (get_highest_layer(layer_state)) {
        case _CDH:
            oled_write_P(PSTR("Q W F P B\n"), false);
            oled_write_P(PSTR("*-*-*\n"), false);
            oled_write_P(PSTR("-> Colemak-DH\n"), false);
            break;
        case _LOWER:
            oled_write_P(PSTR("numpad\n"), false);
            oled_write_P(PSTR("*-*-*\n"), false);
            oled_write_P(PSTR("-> Num\n"), false);
            break;
        case _RAISE:
            oled_write_P(PSTR("! @ # $ %\n"), false);
            oled_write_P(PSTR("*-*-*\n"), false);
            oled_write_P(PSTR("-> Util\n"), false);
            break;
        case _ADJUST:
            oled_write_P(PSTR("special\n"), false);
            oled_write_P(PSTR("*-*-*\n"), false);
            oled_write_P(PSTR("Adjusting...\n"), false);
            break;
        default:
            oled_write_P(PSTR("other"), false);
            oled_write_P(PSTR("wwwww\n"), false);
            oled_write_P(PSTR("what did you do...\n"), false);
    }
}

void oled_task_user(void) {
    if (is_keyboard_master()) {
        render_status();
    } else {
        render_message();
    }
}

