bool encoder_update_user(uint8_t index, bool clockwise) {
    if (index == 0) { /* First encoder */
        switch (get_highest_layer(layer_state)) {
            case _CDH:
                if (clockwise) {
                    tap_code16(C(KC_Y));
                } else {
                    tap_code16(C(KC_Z));
                }
                break;
            case _RAISE:
                if (clockwise) {
                    tap_code(KC_TAB);
                } else {
                    tap_code(KC_BSPC);
                }
                break;
            case _LOWER:
                if (clockwise) {
                    tap_code(KC_VOLU);
                } else {
                    tap_code(KC_VOLD);
                }
                break;
            case _ADJUST:
                break;
            default:
                break;
        }
    }
    return true;
}
